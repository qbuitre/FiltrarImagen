    private void allCheckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_allCheckActionPerformed
        if(allCheck.isSelected()) {
            redCheck.setSelected(true);
            greenCheck.setSelected(true);
            blueCheck.setSelected(true);
            lienzo.changeColorFilter(true, true, true);
        }
        allCheck.setSelected(true);
    }//GEN-LAST:event_allCheckActionPerformed

    private void blueCheckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_blueCheckActionPerformed
        if(!blueCheck.isSelected()) {
            if(redCheck.isSelected() || greenCheck.isSelected()) {
                blueCheck.setSelected(false);
                allCheck.setSelected(false);
                lienzo.changeColorFilter(redCheck.isSelected(), greenCheck.isSelected(), false);
            } else {
                blueCheck.setSelected(true);
                JOptionPane.showMessageDialog(null, "Debes marcar al menos uno");
            }
        } else {
            blueCheck.setSelected(true);
            if(redCheck.isSelected() && greenCheck.isSelected()) {
                allCheck.setSelected(true);
            }
            lienzo.changeColorFilter(redCheck.isSelected(), greenCheck.isSelected(), true);
        }
    }//GEN-LAST:event_blueCheckActionPerformed

    private void redCheckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_redCheckActionPerformed
        if(!redCheck.isSelected()) {
            if(greenCheck.isSelected() || blueCheck.isSelected()) {
                redCheck.setSelected(false);
                allCheck.setSelected(false);
                lienzo.changeColorFilter(false, greenCheck.isSelected(), blueCheck.isSelected());
            } else {
                redCheck.setSelected(true);
                JOptionPane.showMessageDialog(null, "Debes marcar al menos uno");
            }
        } else {
            redCheck.setSelected(true);
            if(greenCheck.isSelected() && blueCheck.isSelected()) {
                allCheck.setSelected(true);
            }
            lienzo.changeColorFilter(true, greenCheck.isSelected(), blueCheck.isSelected());
        }
    }//GEN-LAST:event_redCheckActionPerformed

    private void greenCheckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_greenCheckActionPerformed
        if(!greenCheck.isSelected()) {
            if(redCheck.isSelected() || blueCheck.isSelected()) {
                greenCheck.setSelected(false);
                allCheck.setSelected(false);
                lienzo.changeColorFilter(redCheck.isSelected(), false, blueCheck.isSelected());
            } else {
                greenCheck.setSelected(true);
                JOptionPane.showMessageDialog(null, "Debes marcar al menos uno");
            }
        } else {
            greenCheck.setSelected(true);
            if(redCheck.isSelected() && blueCheck.isSelected()) {
                allCheck.setSelected(true);
            }
            lienzo.changeColorFilter(redCheck.isSelected(), true, blueCheck.isSelected());
        }
    }//GEN-LAST:event_greenCheckActionPerformed

